<?php

namespace Civix\CoreBundle\Model\Avatar;

class DefaultAvatar implements DefaultAvatarInterface
{
    /**
     * @var string
     */
    private $path;

    public function __construct(string $path)
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }
}