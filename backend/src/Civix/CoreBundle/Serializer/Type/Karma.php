<?php

namespace Civix\CoreBundle\Serializer\Type;

use Civix\CoreBundle\Entity\User;

class Karma
{
    /**
     * @var User
     */
    private $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }
}